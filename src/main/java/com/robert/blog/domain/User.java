package com.robert.blog.domain;

/**
 * 用户实体
 */
public class User {
    /**
     * 用户唯一标识
     */
    private Long id;
    /**
     * 用户名
     */
    private String name;
    /**
     * 用户email
     */
    private String email;

    public User() {
    }

    public User(Long id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public User(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public Long getId() {

        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
